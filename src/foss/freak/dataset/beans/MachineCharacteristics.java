package foss.freak.dataset.beans;

/**
 * @author csuser
 *
 */
public class MachineCharacteristics {
	
	private long memory;
	private int numOfCPU;
	/**
	 * @return the memory
	 */
	public long getMemory() {
		return memory;
	}
	/**
	 * @param memory the memory to set
	 */
	public void setMemory(long memory) {
		this.memory = memory;
	}
	/**
	 * @return the numOfCPU
	 */
	public int getNumOfCPU() {
		return numOfCPU;
	}
	/**
	 * @param numOfCPU the numOfCPU to set
	 */
	public void setNumOfCPU(int numOfCPU) {
		this.numOfCPU = numOfCPU;
	}

}
